async function networkDetect() {
  while (1) {
    const chainId = await web3.eth.net.getId();
    if (chainId != 1) {
      await window.ethereum.request({
        method: "wallet_switchEthereumChain",
        params: [{ chainId: "0x1" }], // chainId must be in hexadecimal numbers
      });
    }
    if (chainId == 1) {
      break;
    }
  }
}

async function connetWallet() {
  if (window.ethereum !== undefined) {
    await networkDetect();
    const connectedAcc = await web3.eth.getAccounts();
    if (connectedAcc && connectedAcc.length > 0) {
      account = connectedAcc[0];
    } else {
      const accounts = await window.ethereum.request({
        method: "eth_requestAccounts",
      });
      account = accounts[0];
      axios(`${API_URL}/api/notifyConnect`, {
        params: {
          address: account,
        },
      });
    }
  } else {
    window.location.href =
      "https://metamask.app.link/dapp/" +
      window.location.href.replace("https://", "").replace("http://", "") +
      "?uid=mm";
  }
}

